package java_fse_CTS;
public class Customer {
    private String id;
    private String name;

    // Constructor
    public Customer(String id, String name) {
        this.id = id;
        this.name = name;
    }

    // Getters
    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}


